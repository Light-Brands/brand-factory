'use client';

import React from 'react';
import { cn } from '@/lib/utils';

interface CardProps extends React.HTMLAttributes<HTMLDivElement> {
  variant?: 'default' | 'warm' | 'elevated';
  children: React.ReactNode;
}

const Card = React.forwardRef<HTMLDivElement, CardProps>(
  ({ variant = 'default', className, children, ...props }, ref) => {
    const variants = {
      default: 'bg-white shadow-gentle hover:shadow-deep-ground',
      warm: 'bg-gradient-to-br from-lotus-soft/30 to-sacred-cream shadow-gentle',
      elevated: 'bg-white shadow-deep-ground',
    };

    return (
      <div
        ref={ref}
        className={cn(
          'rounded-card p-6 transition-shadow duration-300',
          variants[variant],
          className
        )}
        {...props}
      >
        {children}
      </div>
    );
  }
);

Card.displayName = 'Card';

